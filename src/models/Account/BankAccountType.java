package models.Account;

/**
 * The {@code BankAccountType} enum represents the types of bank accounts,
 * distinguishing between a Savings account and a Checking account.
 **/
public enum BankAccountType {
    /**
     * Represents a Savings account type.
     */
    Saving,
    /**
     * Represents a Checking account type.
     */
    Checking;
}
